package com.fucongzheng.oop;

public abstract class DemoAbstract {
    /*
    抽象类
    定义抽象类：使用 abstract 关键字来声明一个抽象类
    抽象方法：抽象类可以包含抽象方法，这些方法没有具体的实现，只有方法的声明。抽象方法用 abstract 关键字标记，并且不能在抽象类中直接调用
    继承抽象类：其他类可以通过继承抽象类来使用其属性和方法。子类必须实现抽象类中的所有抽象方法，除非子类本身也是抽象类
    无法实例化：由于抽象类存在抽象方法，无法直接实例化抽象类对象。只能通过实例化具体的子类对象来使用抽象类的功能

    抽象类提供了一种在继承关系中实现代码重用和多态性的机制。它可以作为设计模式的基础，将共享的行为放在抽象类中，并由具体的子类进行定制化实现
     */
    public abstract void abstractMethod();
}
